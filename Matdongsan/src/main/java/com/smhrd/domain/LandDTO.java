package com.smhrd.domain;

public class LandDTO {
	
	private String APT_no;
	private String APT_name;
	private String APT_price;
	private String APT_price2;
	private String APT_addr;
	private String APT_addr2;
	private String APT_info;
	private String APT_char;
	private String APT_geo;
	private String APT_ex;
	

	public LandDTO(String aPT_no, String aPT_name, String aPT_price, String aPT_price2, String aPT_addr,
			String aPT_addr2, String aPT_info, String aPT_char, String aPT_geo, String aPT_ex) {
		super();
		APT_no = aPT_no;
		APT_name = aPT_name;
		APT_price = aPT_price;
		APT_price2 = aPT_price2;
		APT_addr = aPT_addr;
		APT_addr2 = aPT_addr2;
		APT_info = aPT_info;
		APT_char = aPT_char;
		APT_geo = aPT_geo;
		APT_ex = aPT_ex;
	}


	public String getAPT_no() {
		return APT_no;
	}


	public void setAPT_no(String aPT_no) {
		APT_no = aPT_no;
	}


	public String getAPT_name() {
		return APT_name;
	}


	public void setAPT_name(String aPT_name) {
		APT_name = aPT_name;
	}


	public String getAPT_price() {
		return APT_price;
	}


	public void setAPT_price(String aPT_price) {
		APT_price = aPT_price;
	}


	public String getAPT_price2() {
		return APT_price2;
	}


	public void setAPT_price2(String aPT_price2) {
		APT_price2 = aPT_price2;
	}


	public String getAPT_addr() {
		return APT_addr;
	}


	public void setAPT_addr(String aPT_addr) {
		APT_addr = aPT_addr;
	}


	public String getAPT_addr2() {
		return APT_addr2;
	}


	public void setAPT_addr2(String aPT_addr2) {
		APT_addr2 = aPT_addr2;
	}


	public String getAPT_info() {
		return APT_info;
	}


	public void setAPT_info(String aPT_info) {
		APT_info = aPT_info;
	}


	public String getAPT_char() {
		return APT_char;
	}


	public void setAPT_char(String aPT_char) {
		APT_char = aPT_char;
	}


	public String getAPT_geo() {
		return APT_geo;
	}


	public void setAPT_geo(String aPT_geo) {
		APT_geo = aPT_geo;
	}


	public String getAPT_ex() {
		return APT_ex;
	}


	public void setAPT_ex(String aPT_ex) {
		APT_ex = aPT_ex;
	}
	
}
